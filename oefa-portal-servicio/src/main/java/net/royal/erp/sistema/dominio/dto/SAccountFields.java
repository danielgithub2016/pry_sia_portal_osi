package net.royal.erp.sistema.dominio.dto;

public class SAccountFields {

	private String cuentacontable;
	private String centrocosto;
	private String centrocostodestino;
	private String element;
	private String sucursal;

	/**
	 * @return the cuentacontable
	 */
	public String getCuentacontable() {
		return cuentacontable;
	}

	/**
	 * @param cuentacontable
	 *            the cuentacontable to set
	 */
	public void setCuentacontable(String cuentacontable) {
		this.cuentacontable = cuentacontable;
	}

	/**
	 * @return the centrocosto
	 */
	public String getCentrocosto() {
		return centrocosto;
	}

	/**
	 * @param centrocosto
	 *            the centrocosto to set
	 */
	public void setCentrocosto(String centrocosto) {
		this.centrocosto = centrocosto;
	}

	/**
	 * @return the centrocostodestino
	 */
	public String getCentrocostodestino() {
		return centrocostodestino;
	}

	/**
	 * @param centrocostodestino
	 *            the centrocostodestino to set
	 */
	public void setCentrocostodestino(String centrocostodestino) {
		this.centrocostodestino = centrocostodestino;
	}

	/**
	 * @return the element
	 */
	public String getElement() {
		return element;
	}

	/**
	 * @param element
	 *            the element to set
	 */
	public void setElement(String element) {
		this.element = element;
	}

	/**
	 * @return the sucursal
	 */
	public String getSucursal() {
		return sucursal;
	}

	/**
	 * @param sucursal
	 *            the sucursal to set
	 */
	public void setSucursal(String sucursal) {
		this.sucursal = sucursal;
	}

}
