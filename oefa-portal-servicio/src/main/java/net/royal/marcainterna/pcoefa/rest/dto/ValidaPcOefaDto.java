public class ValidaPcOefaDto {

	private String nombrePcOfea;
	private String usuarioPcOefa;
	private String token;
	public String getNombrePcOfea() {
		return nombrePcOfea;
	}
	public void setNombrePcOfea(String nombrePcOfea) {
		this.nombrePcOfea = nombrePcOfea;
	}
	public String getUsuarioPcOefa() {
		return usuarioPcOefa;
	}
	public void setUsuarioPcOefa(String usuarioPcOefa) {
		this.usuarioPcOefa = usuarioPcOefa;
	}
	public String getToken() {
		return token;
	}
	public void setToken(String token) {
		this.token = token;
	}
	
	
}
